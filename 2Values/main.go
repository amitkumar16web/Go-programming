package main

import "fmt"

func main() {

	fmt.Println("Go" + " " + "Google") // Strings, which can be added together with +.

	fmt.Println("1+6 =", 1+6)          // add Integers and floats.
	fmt.Println("12.0/4.0 =", 7.0/3.0) // div floats.

	fmt.Println(true && false) // check AND condintion
	fmt.Println(true || false) // check OR condintion
	fmt.Println(!true)         // check NOT condintion
}
