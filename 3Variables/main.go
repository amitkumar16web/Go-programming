package main

import "fmt"

func main() {

	var i int     // i values is zero
	var a int = 1 //var declares 1 or more variables.
	var b = 2
	c := 3
	var d, e, f = 10.5, "amit", 11 //You can declare multiple variables at once
	g, h := 12.5, "kumar"

	j := true

	fmt.Println(i, " ", a, " ", b, " ", c)
	fmt.Println(d, " ", e, " ", f)
	fmt.Println(g, " ", h)
	fmt.Println(j)

}
